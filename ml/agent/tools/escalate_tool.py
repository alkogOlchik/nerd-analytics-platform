"""Tool for transferring the conversation to a human operator."""

from __future__ import annotations

from typing import Any, Dict

from langchain_core.tools import tool


@tool
def escalate_to_operator(reason: str = "") -> Dict[str, Any]:
    """Передать обращение оператору-человеку.

    Используй этот инструмент когда:
    - Вопрос выходит за рамки базы знаний и твоих возможностей
    - Ты не уверен в корректности своего ответа
    - Проблема требует ручного вмешательства (баг на стороне сервиса, ошибка в данных)
    - Пользователь явно просит поговорить с человеком
    - После двух попыток уточнить проблему ты всё ещё не можешь помочь

    После вызова этого инструмента напиши пользователю короткое дружелюбное сообщение
    о том, что передаёшь его оператору.

    Args:
        reason: Краткое внутреннее объяснение причины передачи (не показывается пользователю).
    """
    return {
        "escalated": True,
        "reason": reason or "Проблема вне области знаний ИИ-ассистента",
    }
